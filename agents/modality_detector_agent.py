from crewai import Agent

modality_detector_agent = Agent(
    name="ModalityDetectorAgent",
    role="Агент определения модальности входных данных",
    goal="""
        Определить, что именно было загружено пользователем: текст, изображение, аудиофайл или видео.
        Вернуть модальность в стандартизированном виде: text / image / audio / video.
    """,
    backstory="""
        Ты — агент, отвечающий за анализ входных данных и классификацию их по типу (модальности).
        Используешь доступные метаданные, расширения файлов, сигнатуры и ключевые признаки.
    """,
    tools=[],
    verbose=True,
)
